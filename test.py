# Concatenation
test1 = "Name"
test2 = "Name2"

result = test1 + " " + test2
print("Concatenation")
print(result, "\n")

# Extraction
test3 = "Test Name"
extract = test3[0:4]
print("Extract")
print(extract,  "\n")

# Joining
test4 = ["apple", "banana", "kiwi"]
test5 = ", ".join(test4)
print("Joining")
print(test5, "\n")

# Split
test6 = "Carl Timothy"
test7 = test6.split(" ")
print("Split")
print(test7, "\n")

# Length
test8 = "This is a test"
print(len(test8))

# Initialization
test9 = "Initialize"
print(test9)

